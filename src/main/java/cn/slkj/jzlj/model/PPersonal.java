package cn.slkj.jzlj.model;

public class PPersonal {

	private String id;
	private String name;//发现人
	private String carId;//车牌id
	private String selfNumber;//自编号
	private String carNumber;//车牌号
	private String pecType;//违章类型
	private String behavior;//违章行为
	private String pecTime;//违章时间
	private  String place;//违章地点
	private  String state;//违章状态
	private String djTime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCarId() {
		return carId;
	}
	public void setCarId(String carId) {
		this.carId = carId;
	}
	public String getCarNumber() {
		return carNumber;
	}
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	public String getPecType() {
		return pecType;
	}
	public void setPecType(String pecType) {
		this.pecType = pecType;
	}
	public String getBehavior() {
		return behavior;
	}
	public void setBehavior(String behavior) {
		this.behavior = behavior;
	}
	public String getPecTime() {
		return pecTime;
	}
	public void setPecTime(String pecTime) {
		this.pecTime = pecTime;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getDjTime() {
		return djTime;
	}
	public void setDjTime(String djTime) {
		this.djTime = djTime;
	}

	public String getSelfNumber() {
		return selfNumber;
	}

	public void setSelfNumber(String selfNumber) {
		this.selfNumber = selfNumber;
	}

	@Override
	public String toString() {
		return "PPersonal [id=" + id + ", name=" + name + ", carId=" + carId + ", carNumber=" + carNumber + ", pecType=" + pecType + ", behavior=" + behavior + ", pecTime=" + pecTime + ", place=" + place + ", state=" + state + ", djTime=" + djTime + "]";
	}
  
}

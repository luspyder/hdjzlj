package cn.slkj.jzlj.model;

public class Department {
 
	private String	id;
	private String d_type;//类型
	private String	d_name;//部门 名 称
	private String	d_fuzeren;//负责人
	private String	d_tel;//联 系 电 话
	private String	d_fax;//传真
	private String	d_add;//地址
	private String	d_email;//email
	private String	d_miaoshu;//描述
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getD_type() {
		return d_type;
	}
	public void setD_type(String d_type) {
		this.d_type = d_type;
	}
	public String getD_name() {
		return d_name;
	}
	public void setD_name(String d_name) {
		this.d_name = d_name;
	}
	public String getD_fuzeren() {
		return d_fuzeren;
	}
	public void setD_fuzeren(String d_fuzeren) {
		this.d_fuzeren = d_fuzeren;
	}
	public String getD_tel() {
		return d_tel;
	}
	public void setD_tel(String d_tel) {
		this.d_tel = d_tel;
	}
	public String getD_fax() {
		return d_fax;
	}
	public void setD_fax(String d_fax) {
		this.d_fax = d_fax;
	}
	public String getD_add() {
		return d_add;
	}
	public void setD_add(String d_add) {
		this.d_add = d_add;
	}
	public String getD_email() {
		return d_email;
	}
	public void setD_email(String d_email) {
		this.d_email = d_email;
	}
	public String getD_miaoshu() {
		return d_miaoshu;
	}
	public void setD_miaoshu(String d_miaoshu) {
		this.d_miaoshu = d_miaoshu;
	}
	@Override
	public String toString() {
		return "Department [id=" + id + ", d_type=" + d_type + ", d_name=" + d_name + ", d_fuzeren=" + d_fuzeren + ", d_tel=" + d_tel + ", d_fax=" + d_fax + ", d_add=" + d_add + ", d_email=" + d_email + ", d_miaoshu=" + d_miaoshu + "]";
	}
 
	
}
